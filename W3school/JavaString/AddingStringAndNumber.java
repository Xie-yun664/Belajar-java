class AddingStringAndNumber {
  public static void main(String[] args) {
    int x = 12;
    int y = 32;
    int z = x + y;
    System.out.println(z);
    
    String a = "20";
    String b = "46";
    String c = a + b;
    System.out.println(c);
    
    String s = "23";
    int i = 21;
    String h = s + i;
    System.out.println(h);
  }
}